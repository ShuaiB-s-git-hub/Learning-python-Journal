# Python中的注释
#
# 注释
# 在代码中对代码的功能进行解释说明的标注性文字,可以提高代码的可读性
# 注释的内容会被Python解释器忽略
#
# 通常包括三种类型的注释
#
# 单行注释 以"#"开头,直到换行结束
#
# 多行注释之并没有单独的多行注释标记,将一对三引号之间的代码称为多行注释
# 反正也没有变量接受，这东西就直接当成注释了
#
# 中文编码声明注释→在文件开头加上中文声明注释,用以指定源码文件的编码格式
# 可用记事本打开一个xxx.py文档，然后在另存为中查看默认编码，发现是UTF-8
# 所以可以使用#coding:gbk（得放在第一行）命令来改变文件的编码储存格式（变成ANSI），从而导致中文全变成代码
#    #coding:编码类型   是指将文件编码转成该类型
